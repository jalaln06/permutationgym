from typing import List, Tuple, Optional

def create_symmetric_group(n: int) -> Tuple[List[List[int]], List[str]]:
    """
    Create generators for the symmetric group Sn.
    Sn can be generated by adjacent transpositions.
    
    Args:
        n: Size of the permutation
        
    Returns:
        Tuple of (generators, generator_names)
    """
    generators = []
    generator_names = []
    
    for i in range(n-1):
        # Generate adjacent transposition (i, i+1)
        gen = list(range(n))
        gen[i], gen[i+1] = gen[i+1], gen[i]
        generators.append(gen)
        generator_names.append(f"swap_{i}_{i+1}")
        
    return generators, generator_names