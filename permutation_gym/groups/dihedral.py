from typing import List, Tuple

def create_dihedral_group(n: int) -> Tuple[List[List[int]], List[str]]:
    """
    Create generators for the dihedral group Dn.
    Dn can be generated by a rotation and a reflection.
    
    Args:
        n: Size of the regular polygon (number of vertices)
        
    Returns:
        Tuple of (generators, generator_names)
    """
    # Rotation generator (rotate by one position)
    rotation = [1] + list(range(2, n)) + [0]  # (0,1,2,...,n-1)
    
    # Reflection generator
    reflection = list(range(n))
    for i in range(n // 2):
        reflection[i], reflection[n-1-i] = reflection[n-1-i], reflection[i]
    
    return [rotation, reflection], ["rotation", "reflection"]