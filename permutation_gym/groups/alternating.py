from typing import List, Tuple

def create_alternating_group(n: int) -> Tuple[List[List[int]], List[str]]:
    """
    Create generators for the alternating group An.
    An can be generated by 3-cycles.
    
    Args:
        n: Size of the permutation
        
    Returns:
        Tuple of (generators, generator_names)
    """
    generators = []
    generator_names = []
    
    for i in range(n-2):
        # Generate 3-cycle (i, i+1, i+2)
        gen = list(range(n))
        gen[i], gen[i+1], gen[i+2] = gen[i+1], gen[i+2], gen[i]
        generators.append(gen)
        generator_names.append(f"cycle_{i}_{i+1}_{i+2}")
        
    return generators, generator_names