from typing import List, Tuple

def create_cyclic_group(n: int) -> Tuple[List[List[int]], List[str]]:
    """
    Create generators for the cyclic group of order n.
    A cyclic group can be generated by a single n-cycle.
    
    Args:
        n: Size of the cycle
        
    Returns:
        Tuple of (generators, generator_names)
    """
    # Generate a single n-cycle (0,1,2,...,n-1)
    gen = [1] + list(range(2, n)) + [0]
    return [gen], ["cycle"]